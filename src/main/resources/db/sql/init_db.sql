CREATE TABLE BEER_SERVICE.beers (
    id VARCHAR(36) NOT NULL PRIMARY KEY,
    version BIGINT,
    created_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    last_modified_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP NOT NULL,
    beer_name VARCHAR(255),
    beer_style VARCHAR(255),
    upc VARCHAR(255) UNIQUE,
    price DECIMAL(19,2),
    min_on_hand INT,
    quantity_to_beer INT
);